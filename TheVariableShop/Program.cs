
// Byte
byte aByte = 97;
Console.WriteLine(aByte);
aByte = 123;
Console.WriteLine(aByte);

// Sbyte
sbyte anSbyte = -13;
Console.WriteLine(anSbyte);
anSbyte = 13;
Console.WriteLine(anSbyte);

// short
short aShort = 1010;
Console.WriteLine(aShort);
aShort = 999;
Console.WriteLine(aShort);

// Ushort
ushort anUshort = 1300;
Console.WriteLine(anUshort);
anUshort = 10;
Console.WriteLine(anUshort);

// Int
int anInt = 200124;
Console.WriteLine(anInt);
anInt = 3;
Console.WriteLine(anInt);

// Uint
uint anUInt = 133021;
Console.WriteLine(anUInt);
anUInt = 3;
Console.WriteLine(anUInt);

// Long
long aLong = 22000010022L;
Console.WriteLine(aLong);
aLong = 4L;
Console.WriteLine(aLong);

// Ulong
ulong anUlong = 32895823573295UL;
Console.WriteLine(anUlong);
anUlong = 4UL;
Console.WriteLine(anUlong);

// Float
float aFloat = 10.7f;
Console.WriteLine(aFloat);
aFloat = 3.0f;
Console.WriteLine(aFloat);

// Double
double aDouble = 123.25158;
Console.WriteLine(aDouble);
aDouble = 3.0;
Console.WriteLine(aDouble);

// Decimal
decimal aDecimal = 0.000000000011m;
Console.WriteLine(aDecimal);
aDecimal = 0.1m;
Console.WriteLine(aDecimal);

// Char
char aChar = 'a';
Console.WriteLine(aChar);
aChar = 'b';
Console.WriteLine(aChar);

// String
string aString = "Hello, world";
Console.WriteLine(aString);
aString = "A string";
Console.WriteLine(aString);

// Bool
bool aBool = true;
Console.WriteLine(aBool);
aBool = false;
Console.WriteLine(aBool);
